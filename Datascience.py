class LaunchRequestHandler(AbstractRequestHandler):
    """Handler for Skill Launch."""
    def can_handle(self, handler_input):
        # type: (HandlerInput) -> bool

        return ask_utils.is_request_type("LaunchRequest")(handler_input)

    def handle(self, handler_input):
        # type: (HandlerInput) -> Response
        speak_output = "HEY WELCOME Now you can ask regarding Data Science. I'm here to asist you"

        return (
            handler_input.response_builder
                .speak(speak_output)
                .ask(speak_output)
                .response
        )



class Rintenthandler(AbstractRequestHandler):
    def can_handle(self, handler_input):
        return ask_utils.is_intent_name("LaunchRequest")(handler_input) or ask_utils.is_intent_name("Rintent")(handler_input)
    def handle(self, handler_input):
        speak_output = random.choice(list)
        return (handler_input.response_builder.speak(speak_output).ask(speak_output).response)


